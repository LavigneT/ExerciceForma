package Main.formes;

public class Point2D {
	
	private float x, y;
	
	public Point2D(float x, float y) {
		this.x = x;
		this.y = y;
	}
	
	
	
	//getters and setters
	
	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
	
	
}
